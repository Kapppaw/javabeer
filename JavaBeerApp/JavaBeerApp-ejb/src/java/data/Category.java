package data;

import java.io.Serializable;
import java.util.Collection;
import javax.persistence.Basic;
import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author Thibault
 */
@Entity
@Table(name = "CATEGORY")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Category.findAll", query = "SELECT c FROM Category c"),
    @NamedQuery(name = "Category.findById", query = "SELECT c FROM Category c WHERE c.id = :id"),
    @NamedQuery(name = "Category.findByUrlimage", query = "SELECT c FROM Category c WHERE c.urlimage = :urlimage")})
public class Category implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "ID")
    private Integer id;
    @Size(max = 255)
    @Column(name = "URLIMAGE")
    private String urlimage;
    @OneToMany(cascade = CascadeType.ALL, mappedBy = "categoryid")
    private Collection<Translatecategory> translatecategoryCollection;
    @OneToMany(cascade = CascadeType.ALL, mappedBy = "categoryid")
    private Collection<Categoryoftheitem> categoryoftheitemCollection;

    public Category() {
    }

    public Category(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getUrlimage() {
        return urlimage;
    }

    public void setUrlimage(String urlimage) {
        this.urlimage = urlimage;
    }

    @XmlTransient
    public Collection<Translatecategory> getTranslatecategoryCollection() {
        return translatecategoryCollection;
    }

    public void setTranslatecategoryCollection(Collection<Translatecategory> translatecategoryCollection) {
        this.translatecategoryCollection = translatecategoryCollection;
    }

    @XmlTransient
    public Collection<Categoryoftheitem> getCategoryoftheitemCollection() {
        return categoryoftheitemCollection;
    }

    public void setCategoryoftheitemCollection(Collection<Categoryoftheitem> categoryoftheitemCollection) {
        this.categoryoftheitemCollection = categoryoftheitemCollection;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Category)) {
            return false;
        }
        Category other = (Category) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "data.Category[ id=" + id + " ]";
    }
    
}
